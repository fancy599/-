const rows = JSON.parse(fs.readFileSync(target, "utf8"));
let changed = 0;
for (const row of rows) {
  const topic = TOPIC_BY_CODE[row.standard_code];
  if (topic && row.control_topic !== topic) {
    row.control_topic = topic;
    row.version = "V2026.2";
    changed += 1;
  }
}

const seen = new Map();
for (const row of rows) {
  const key = `${row.business_domain}\u0000${row.control_topic}`;
  if (seen.has(key)) {
    throw new Error(`领域内控制主题仍重复：${row.business_domain}/${row.control_topic}（${seen.get(key)}、${row.standard_code}）`);
  }
  seen.set(key, row.standard_code);
}

